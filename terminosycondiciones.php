        <strong>¿Qué es un certificado de seguridad?</strong>
        <p>Al acceder a un sitio web que utiliza HTTPS (seguridad de conexión), el servidor utiliza un certificado para demostrar la identidad del sitio web a los navegadores (como Chrome). Para que puedas navegar por la Web de forma segura, Chrome solicita a los sitios web que utilicen certificados de organizaciones de confianza.</p>
        <strong>¿Por qué el Certificado SSL Vtex?</strong>
        <p>El protocolo SSL (Secure Socket Layer) es un protocolo desarrollado para aumentar la seguridad de los datos transmitidos a través de Internet. Las conexiones a través de SSL se recomiendan especialmente para el envío de información tal como números de tarjetas de crédito, contraseñas y cualquier otra información sensible a través de Internet.</p>
